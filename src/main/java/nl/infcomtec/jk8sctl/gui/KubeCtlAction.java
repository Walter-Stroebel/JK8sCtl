    private void butOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butOkActionPerformed
        butOk.setEnabled(false);
        butOk.setVisible(false);
        butOkClose.setEnabled(false);
        butOkClose.setVisible(false);
        butClose.setText("Close");
        jTextArea1.setText("Running...");
        try {
            RunKubeCtl delete = RunKubeCtl.delete(metadata);
            if (delete.err.length() > 0) {
                jTextArea1.setText("Error(s):\n" + delete.err);
            } else {
                jTextArea1.setText(delete.in.toString() + "\n" + "Return status: " + delete.ret);
            }
        } catch (Exception ex) {
            Logger.getLogger(ItemSelector.class.getName()).log(Level.SEVERE, null, ex);
            jTextArea1.setText("Exception: " + ex.toString());
        }
    }//GEN-LAST:event_butOkActionPerformed

