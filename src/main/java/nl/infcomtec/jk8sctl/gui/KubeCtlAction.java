    private void butOkCloseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butOkCloseActionPerformed
        butOk.setEnabled(false);
        butOk.setVisible(false);
        butOkClose.setEnabled(false);
        butOkClose.setVisible(false);
        butClose.setText("Close");
        new Thread() {
            @Override
            public void run() {
                try {
                    RunKubeCtl delete = RunKubeCtl.delete(metadata);
                } catch (Exception ex) {
                    Logger.getLogger(ItemSelector.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }.start();
        dispose();
    }//GEN-LAST:event_butOkCloseActionPerformed

    public static void delete(Metadata selected) {
        /* Create and display the form */
        java.awt.EventQueue.invokeLater(new Runnable() {
            @Override
            public void run() {
                KubeCtlAction kubeCtlAction = new KubeCtlAction("Really delete " + selected.getKind() + " in namespace " + selected.getNamespace() + " named " + selected.getName() + "?");
                kubeCtlAction.metadata = selected;
                kubeCtlAction.setVisible(true);
            }
        });
    }

